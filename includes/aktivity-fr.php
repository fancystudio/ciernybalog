<p>
La commune Čierny Balog est située dans le cadre magnifique des collines Vepor, dans la vallée de la rivière Čierny Hron, entourée de forêts denses. Le village est situé dans la partie Dobroč, également connue pour la «forêt vierge» de Dobroč, l'une des réserves naturelles nationales et la plus ancienne zone protégée de Slovaquie, classée dans le réseau européen des sites ayant une grande valeur patrimoniale, Natura 2000.Compte tenu de son emplacement, notre chalet à Čierny Balog est apprécié pendant toute l'année par ses visiteurs qui peuvent pratiquer les sports d’été et d’hiver (VTT, randonnée, ski, snowboard, ski de fond, parapente). Les ramasseurs de champignons trouveront ici de quoi assouvir leur passion…	
</p>
