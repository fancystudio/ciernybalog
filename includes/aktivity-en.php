<p>
The Čierny Balog community is located in the beautiful area of Veporské vrchy (Vepor Hills), in the valley of the river Čierny Hron (The Black Hron), and in adjacent valleys, surrounded by dense forests. The Čierny Balog cottage is situated in the area named Dobroč which is well known for the Dobroč old-growth forest belonging to the national nature reserves and the oldest protected areas on the Slovak territory, and forming part of NATURA 2000 network of important European sites.For its position the Čierny Balog Cottage is sought after yearly because fans of mountain biking, hiking, snowboarding, cross-country skiing, mashrooming, or paragliding shall come into their own.   
</p>
